package kr.utila.rpg.systems.stat.objects;

public class StatSetting {

    private int healthPerLevel;
    private int pointPerLevel;
    private double strengthDamage;
    private double strengthDamagePercentage;
    private double strengthHealth;
    private double agilityDamage;
    private double agilityDamagePercentage;
    private double agilitySpeedPercentage;
    private double agilityCriticalChance;
    private double hpDamage;
    private double hpDamageToMonster;
    private double hpHealth;
    private double hpDefensePercentage;
    private double intelligenceDamage;
    private double intelligenceSkillDamagePercentage;
    private double intelligenceHealth;

    public int getHealthPerLevel() {
        return healthPerLevel;
    }

    public int getPointPerLevel() {
        return pointPerLevel;
    }

    public double getStrengthDamage() {
        return strengthDamage;
    }

    public double getStrengthDamagePercentage() {
        return strengthDamagePercentage;
    }

    public double getStrengthHealth() {
        return strengthHealth;
    }

    public double getAgilityDamage() {
        return agilityDamage;
    }

    public double getAgilityDamagePercentage() {
        return agilityDamagePercentage;
    }

    public double getAgilitySpeedPercentage() {
        return agilitySpeedPercentage;
    }

    public double getAgilityCriticalChance() {
        return agilityCriticalChance;
    }

    public double getHPDamage() {
        return hpDamage;
    }

    public double getHPDamageToMonster() {
        return hpDamageToMonster;
    }

    public double getHPHealth() {
        return hpHealth;
    }

    public double getHpDefensePercentage() {
        return hpDefensePercentage;
    }

    public double getIntelligenceDamage() {
        return intelligenceDamage;
    }

    public double getIntelligenceSkillDamagePercentage() {
        return intelligenceSkillDamagePercentage;
    }

    public double getIntelligenceHealth() {
        return intelligenceHealth;
    }
}
